 * @mixin \Eloquent
 */
class Category extends Basemodel
{
    protected $has_uuid = true;

    public function getRouteKeyName()
    {
        return 'uuid';
    }

    public function user(){
        return $this->belongsTo(User::class);
    }

    public function posts(){
        return $this->hasMany(Post::class);
    }
